<?php 

$str = "Hello"; 
echo $str ;

echo "<br>";

for ($i = 1; $i <= 2; $i ++) { 
    // $iが2回ループするので2 * 2 = 4回ループします 
    for ($j = 1; $j <= 2; $j ++) { 
        // 2 * 2 * 2 = 8回ループします 
        for ($k = 1; $k <= 2; $k ++) { 
            // 2 * 2 * 2 * 2 = 16回ループします 
            for ($l = 1; $l <= 2; $l ++) { 
                // $lが1, 2の順番で出力されます 
                echo $l . " "; 
            }
        }
    }
} 

?>